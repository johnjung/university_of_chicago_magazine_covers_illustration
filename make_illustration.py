def get_imageserver_url(imageserver_url, ark, n, w, h):
    """Get an image tile URL from our imageserver.
        Args:
           imageserver_url: A string, the protocol and hostname for the
                            imageserver. E.g.,
                            'https://iiif-server.lib.uchicago.edu' 
           ark:             A string, the ark identifier, with no suffix.
                            E.g., 'ark:61001/b25p3r42st00'
           n:               An integer, the image number. E.g., 1.
           w:               An integer, the image width to request, or None
                            to calculate the correct proportions based on
                            the height.
           h:               An integer, the image height to request, or None
                            to calculate the correct proportions based on
                            the width.

        Returns:
           A string, the imagesever URL.
    """
    return '{}/{}%2F{:08d}/full/{},{}/0/default.jpg'.format(
        imageserver_url,
        urllib.parse.quote(ark),
        n,
        '' if w == None else str(w),
        '' if h == None else str(h)
    )

